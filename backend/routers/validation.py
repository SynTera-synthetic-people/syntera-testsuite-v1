def _run_comparison(survey: Survey, synthetic: list[float], real: list[float], db: Session):
    """Shared logic to run comparison and persist results."""
    results = engine.compare_distributions(synthetic or [], real or [])
    
    # Sanitize results to remove NaN/inf values
    results = sanitize_for_json(results)
    
    # Use the calculated overall_accuracy from the comparison engine, not hardcoded values
    overall_accuracy = results.get("overall_accuracy")
    
    # Fallback to tier-based accuracy if not calculated by engine
    if overall_accuracy is None or (isinstance(overall_accuracy, float) and math.isnan(overall_accuracy)):
        overall_tier = results.get("overall_tier", "TIER_4")
        # Use tier-based fallback: >85% TIER_1, >75% TIER_2, >50% TIER_3, else TIER_4
        if overall_tier == "TIER_1":
            overall_accuracy = 0.90
        elif overall_tier == "TIER_2":
            overall_accuracy = 0.80
        elif overall_tier == "TIER_3":
            overall_accuracy = 0.62
        else:
            overall_accuracy = 0.45  # TIER_4
    
    # Ensure accuracy is a valid number
    overall_accuracy = float(overall_accuracy) if overall_accuracy is not None else 0.0
    overall_accuracy = max(0.0, min(1.0, overall_accuracy))  # Clamp between 0 and 1
    
    # Log the calculated accuracy for debugging
    logger.info(f"Calculated overall accuracy: {overall_accuracy:.1%} (Tier: {results.get('overall_tier', 'N/A')})")
    
    survey.accuracy_score = overall_accuracy
    survey.validation_status = "VALIDATED"
    survey.test_suite_report = results
    survey.validated_at = datetime.utcnow()
    qc0 = None
    if isinstance(results, dict):
        raw_qc = results.get("question_comparisons")
        if isinstance(raw_qc, list) and len(raw_qc) > 0:
            qc0 = raw_qc
    _sync_survey_study_metrics(survey, db, qc0)

    db.add(
        ValidationRun(
            survey_id=survey.id,
            overall_accuracy=overall_accuracy,
            overall_tier=results.get("overall_tier"),
            question_results=results,
        )
    )
    db.commit()
    
    return {
        "survey_id": survey.id,
        "overall_accuracy": overall_accuracy,
        "tests": results.get("tests", []),
        "test_summary": results.get("test_summary", {}),
        "recommendations": results.get("recommendations", []),
        "synthetic_size": results.get("synthetic_size", 0),
        "real_size": results.get("real_size", 0),
    }


def _estimate_human_time(sample_size: Optional[int]) -> str:
    """Fieldwork duration buckets by respondent count (typical fieldwork)."""
    if not sample_size or sample_size <= 0:
        return "Unknown"
    n = int(sample_size)
    if n <= 2000:
        return "1-2 weeks"
    if n <= 4000:
        return "2-3 weeks"
    if n <= 6000:
        return "3-4 weeks"
    return "4-5 weeks"


def _default_industry_from_study_name(study_name: Optional[str]) -> str:
    txt = (study_name or "").strip().lower()
    if "food" in txt:
        return "Food"
    if re.search(r"\bev\b|electric vehicle|e-vehicle|automotive", txt):
        return "Automotive"
    return "General"


def _study_name_from_file_comparison_title(raw: str) -> Optional[str]:
    """Parse 'File Comparison: X_AI_Summary.csv vs Y_Human_Summary.csv' into a short label."""
    prefix = "File Comparison:"
    s = (raw or "").strip()
    if not s.startswith(prefix):
        return None
    rest = s[len(prefix) :].strip()
    parts = re.split(r"\s+vs\s+", rest, maxsplit=1, flags=re.I)
    first = parts[0].strip() if parts else rest
    if not first:
        return None
    base = first.rsplit(".", 1)[0] if "." in first else first
    base = re.sub(
        r"(?i)_AI_Summary$|_Human_Summary$|_AI$|_Human$|_Synthetic$|_Real$",
        "",
        base,
    )
    name = re.sub(r"[_-]+", " ", base).strip()
    return name or None


def _default_study_name_for_survey(survey: Survey) -> str:
    raw = (survey.title or "").strip()
    food_exact = (
        "File Comparison: Food_Delivery_Behavior_AI_Summary.csv vs "
        "Food_Delivery_Behavior_Human_Summary.csv"
    )
    if raw == food_exact:
        return "Food Delivery Behavior in India"
    parsed = _study_name_from_file_comparison_title(raw)
    if parsed:
        return parsed
    return raw or "Food Delivery Behavior"


def _build_human_economics(sample_size: Optional[int], economics: Optional[HumanStudyEconomicsInput]) -> dict[str, Any]:
    """
    Human survey cost: sample_size × $5 (min) and × $8 (max) per response.
    Effort: standard 80–120 hours for analysis/report prep unless overridden.
    """
    if sample_size and int(sample_size) > 0:
        n = int(sample_size)
        cost_min = float(n * 5)
        cost_max = float(n * 8)
    else:
        cost_min = 0.0
        cost_max = 0.0
    time_range = _estimate_human_time(sample_size)
    effort = "80-120 hours"
    if economics:
        if economics.estimated_cost_min is not None:
            cost_min = float(economics.estimated_cost_min)
        if economics.estimated_cost_max is not None:
            cost_max = float(economics.estimated_cost_max)
        time_range = economics.estimated_time_range or time_range
        effort = economics.estimated_effort_hours or effort
    return {
        "estimated_cost_min": cost_min,
        "estimated_cost_max": cost_max,
        "estimated_time_range": time_range,
        "estimated_effort_hours": effort,
        "cost_per_response_usd_min": 5,
        "cost_per_response_usd_max": 8,
    }


def _normalize_country_name(value: Optional[str]) -> Optional[str]:
    if value is None:
        return None
    txt = str(value).strip()
    if not txt:
        return None
    # Keep the primary country token if region/detail text is appended.
    primary = re.split(r"[|;,/]", txt, maxsplit=1)[0].strip()
    if not primary:
        return txt
    return primary


def _coerce_respondent_count(value: Any) -> Optional[int]:
    if value is None:
        return None
    if isinstance(value, bool):
        return None
    if isinstance(value, (int, float)):
        v = int(value)
        return v if v >= 0 else None
    txt = str(value).strip()
    if not txt:
        return None
    digits = re.sub(r"[^\d]", "", txt)
    if not digits:
        return None
    try:
        return int(digits)
    except ValueError:
        return None


def _coerce_unit_interval(value: Any) -> Optional[float]:
    """Normalize a similarity / accuracy value to 0..1 (accepts 0-100 as percent)."""
    if value is None:
        return None
    try:
        x = float(value)
        if x > 1.0:
            x = x / 100.0
        return max(0.0, min(1.0, x))
    except (TypeError, ValueError):
        return None


