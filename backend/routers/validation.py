@router.get("/test-lab/metrics")
async def get_test_lab_metrics(db: Session = Depends(get_db)):
    surveys = db.query(Survey).all()
    validated_surveys = [s for s in surveys if s.validation_status == "VALIDATED"]
    profiles = db.query(TestLabProfile).all()

    tests_conducted = len(validated_surveys)
    accuracies = [float(s.accuracy_score) for s in validated_surveys if s.accuracy_score is not None]
    avg_similarity = (sum(accuracies) / len(accuracies)) if accuracies else 0.0

    alignments = []
    scenario_counts: dict[str, int] = {}
    industry_counts: dict[str, int] = {}
    for s in validated_surveys:
        report = s.test_suite_report if isinstance(s.test_suite_report, dict) else {}
        alignment = _compute_directional_alignment(report.get("question_comparisons") or [])
        if alignment is not None:
            alignments.append(alignment)
    survey_ids = {s.id for s in surveys}
    for p in profiles:
        if p.survey_id not in survey_ids:
            continue
        if p.scenario:
            scenario_counts[p.scenario] = scenario_counts.get(p.scenario, 0) + 1
        if p.industry:
            industry_counts[p.industry] = industry_counts.get(p.industry, 0) + 1

    avg_directional_alignment = (sum(alignments) / len(alignments)) if alignments else 0.0
    return {
        "tests_conducted": tests_conducted,
        "avg_similarity": max(0.0, min(1.0, avg_similarity)),
        "avg_directional_alignment": max(0.0, min(1.0, avg_directional_alignment)),
        "scenarios_covered": {
            "count": len(scenario_counts),
            "mix": scenario_counts,
        },
        "industries_covered": {
            "count": len(industry_counts),
            "mix": industry_counts,
        },
    }
