def _ensure_profile_defaults(profile: TestLabProfile, survey: Survey) -> bool:
    """Backfill static defaults so frontend always receives persisted profile data."""
    changed = False

    effective_study_name = _default_study_name_for_survey(survey)
    derived_industry = _default_industry_from_study_name(effective_study_name)

    if not profile.geography or str(profile.geography).strip().lower() in {"united states", "usa", "us"}:
        profile.geography = "India"
        changed = True
    if not profile.industry or str(profile.industry).strip().lower() == "general":
        profile.industry = derived_industry
        changed = True
    if not profile.scenario:
        profile.scenario = "General consumer behavior study"
        changed = True

    human = profile.human_study if isinstance(profile.human_study, dict) else {}
    synthetic = profile.synthetic_study if isinstance(profile.synthetic_study, dict) else {}
    verdict = profile.verdict if isinstance(profile.verdict, dict) else {}
    metadata = profile.extra_data if isinstance(profile.extra_data, dict) else {}

    default_sample_size = int(survey.total_personas or 1000)
    default_questions = int(survey.total_questions or 10)
    economics = _build_human_economics(default_sample_size, None)
    expected_human = {
        "survey_name": effective_study_name,
        "target_audience": "Age group: 18-45 years",
        "geography": profile.geography,
        "sample_size": default_sample_size,
        "total_questions": default_questions,
        "economics": economics,
    }
    expected_synthetic = {
        "sample_size": default_sample_size,
        **_synthetic_random_defaults(),
        "economics": {"cost_display": "$999", "time_display": "3-4 hrs", "effort_display": "1-2 hrs"},
        "statistics": {},
    }

    for key, value in expected_human.items():
        if human.get(key) in (None, "", []):
            human[key] = value
            changed = True
    if str(human.get("target_audience", "")).strip().lower() == "survey respondents":
        human["target_audience"] = "Age group: 18-45 years"
        changed = True
    sn = human.get("survey_name")
    if isinstance(sn, str) and sn.strip().startswith("File Comparison:"):
        human["survey_name"] = effective_study_name
        changed = True
    for key, value in expected_synthetic.items():
        if synthetic.get(key) in (None, "", []):
            synthetic[key] = value
            changed = True
    if not verdict:
        verdict = {
            "what_matches": [],
            "where_it_differs": [],
            "why_the_difference": [],
            "summary_statement": None,
            "llm_confidence": None,
            "source": None,
            "llm_model": None,
            "generated_at": None,
        }
        changed = True
    if isinstance(verdict, dict):
        for k in ("summary_statement", "source", "llm_model", "generated_at"):
            if k not in verdict:
                verdict[k] = None
                changed = True
    if not metadata:
        metadata = {}
        changed = True

    if changed:
        profile.human_study = sanitize_for_json(human)
        profile.synthetic_study = sanitize_for_json(synthetic)
        profile.verdict = sanitize_for_json(verdict)
        profile.extra_data = sanitize_for_json(metadata)
    return changed


def _compute_directional_alignment(question_comparisons: list[dict[str, Any]]) -> Optional[float]:
    """Heuristic: alignment is % questions where top synthetic option == top real option."""
    if not question_comparisons:
        return None
    aligned = 0
    comparable = 0
    for q in question_comparisons:
        options = q.get("option_comparisons") or []
        if not options:
            continue
        syn_top = max(options, key=lambda o: float(o.get("synthetic_count", 0) or 0)).get("option")
        real_top = max(options, key=lambda o: float(o.get("real_count", 0) or 0)).get("option")
        comparable += 1
        if str(syn_top) == str(real_top):
            aligned += 1
    if comparable == 0:
        return None
    return aligned / comparable


def _build_rule_based_verdict(
    question_comparisons: list[dict[str, Any]],
) -> dict[str, Any]:
    """
    Deterministic verdict logic:
    - what_matches: count of questions with similarity >= 80%
    - where_it_differs: list questions with similarity < 50%
    - why_the_difference: generic reasons
    - summary_statement: short insight summary
    """
    rows: list[tuple[str, float]] = []
    for q in question_comparisons or []:
        score = _coerce_unit_interval(q.get("match_score"))
        if score is None:
            continue
        qid = str(q.get("question_id") or "").strip()
        qname = str(q.get("question_name") or "").strip() or qid or "Unknown question"
        label = f"Q{qid} — {qname}" if qid else qname
        rows.append((label, score))

    total = len(rows)
    if total == 0:
        return {
            "what_matches": ["No comparable question-level similarity data was available."],
            "where_it_differs": ["No low-similarity question outputs detected because question-level scores are unavailable."],
            "why_the_difference": [
                "Input data granularity may differ between human and synthetic exports.",
                "Question-level mappings may be incomplete or not comparable.",
                "More aligned question metadata is needed for robust similarity scoring.",
            ],
            "summary_statement": (
                "Verdict could not be derived from question-level similarity because no comparable "
                "question scores were found."
            ),
            "llm_confidence": None,
            "source": "rule-based",
            "llm_model": None,
            "generated_at": datetime.utcnow().isoformat(),
        }

    matches = [(label, sc) for (label, sc) in rows if sc >= 0.80]
    differs = sorted([(label, sc) for (label, sc) in rows if sc < 0.50], key=lambda x: x[1])
    avg = sum(sc for _, sc in rows) / total

    what_matches = [
        f"{len(matches)} of {total} questions show >= 80% similarity."
    ]
    if matches:
        top = sorted(matches, key=lambda x: x[1], reverse=True)[:3]
        what_matches.extend([f"{label} ({sc * 100:.1f}% similarity)" for label, sc in top])
    else:
        what_matches.append("No questions crossed the 80% similarity threshold.")

    where_it_differs = [f"{label} ({sc * 100:.1f}% similarity)" for label, sc in differs[:10]]
    if not where_it_differs:
        where_it_differs = ["No question outputs were below 50% similarity."]

    why_the_difference = [
        "Human respondents may exhibit context and recall bias that shifts answer distributions.",
        "Synthetic responses can smooth long-tail behavior, reducing variance on niche options.",
        "Minor wording or calibration differences can amplify divergence on sensitive questions.",
    ]

    summary_statement = (
        f"Across {total} comparable questions, average similarity is {avg * 100:.1f}%. "
        f"{len(matches)} questions are strong matches (>=80%), while {len(differs)} questions "
        f"show meaningful divergence (<50%). Focus analysis on low-similarity questions to refine "
        f"scenario prompts, calibration, and response-mapping assumptions."
    )

    return {
        "what_matches": what_matches,
        "where_it_differs": where_it_differs,
        "why_the_difference": why_the_difference,
        "summary_statement": summary_statement,
        "llm_confidence": None,
        "source": "rule-based",
        "llm_model": None,
        "generated_at": datetime.utcnow().isoformat(),
    }


def _apply_synthetic_study_to_survey_columns(survey: Survey, synthetic: dict[str, Any]) -> None:
    if not synthetic:
        return
    mapping = (
        ("actions_data_points", "actions_data_points"),
        ("neuroscience_data_points", "neuroscience_data_points"),
        ("contextual_layer_data_points", "contextual_layer_data_points"),
    )
    for col, key in mapping:
        v = synthetic.get(key)
        if v is not None:
            try:
                setattr(survey, col, int(v))
            except (TypeError, ValueError):
                pass


def _refresh_checks_passed_from_report(survey: Survey) -> None:
    rep = survey.test_suite_report if isinstance(survey.test_suite_report, dict) else {}
    tests = rep.get("tests") or []
    survey.checks_passed = sum(1 for t in tests if isinstance(t, dict) and not t.get("error"))


def _embed_study_metrics_in_report(survey: Survey) -> None:
    if not isinstance(survey.test_suite_report, dict):
        return
    avg = survey.avg_similarity if survey.avg_similarity is not None else survey.accuracy_score
    sm = sanitize_for_json(
        {
            "actions_data_points": survey.actions_data_points,
            "neuroscience_data_points": survey.neuroscience_data_points,
            "contextual_layer_data_points": survey.contextual_layer_data_points,
            "avg_similarity": avg,
            "directional_alignment": survey.directional_alignment,
            "avg_prediction_accuracy": getattr(survey, "avg_prediction_accuracy", None),
            "avg_relationship_strength": getattr(survey, "avg_relationship_strength", None),
            "checks_passed": survey.checks_passed,
        }
    )
    survey.test_suite_report = {**survey.test_suite_report, "study_metrics": sm}


def _sync_survey_study_metrics(
    survey: Survey,
    db: Session,
    question_comparisons: Optional[list[dict[str, Any]]] = None,
) -> None:
    _refresh_checks_passed_from_report(survey)
    if question_comparisons:
        da = _compute_directional_alignment(question_comparisons)
        if da is not None:
            survey.directional_alignment = da
    profile = db.query(TestLabProfile).filter(TestLabProfile.survey_id == survey.id).first()
    stats: dict[str, Any] = {}
    if profile and isinstance(profile.synthetic_study, dict):
        _apply_synthetic_study_to_survey_columns(survey, profile.synthetic_study)
        raw_stats = profile.synthetic_study.get("statistics")
        if isinstance(raw_stats, dict):
            stats = raw_stats
    if survey.accuracy_score is not None:
        survey.avg_similarity = survey.accuracy_score
    pu = _coerce_unit_interval(stats.get("avg_prediction_accuracy"))
    rs = _coerce_unit_interval(stats.get("avg_relationship_strength"))
    if pu is not None:
        survey.avg_prediction_accuracy = pu
    elif getattr(survey, "avg_prediction_accuracy", None) is None and survey.accuracy_score is not None:
        survey.avg_prediction_accuracy = survey.accuracy_score
    if rs is not None:
        survey.avg_relationship_strength = rs
    elif getattr(survey, "avg_relationship_strength", None) is None and survey.accuracy_score is not None:
        survey.avg_relationship_strength = survey.accuracy_score
    # Auto-generate deterministic verdict from question-level similarities unless explicitly manual.
    if question_comparisons:
        if not profile:
            profile = TestLabProfile(survey_id=survey.id)
            db.add(profile)
        existing_verdict = profile.verdict if isinstance(profile.verdict, dict) else {}
        source = str(existing_verdict.get("source") or "").strip().lower()
        if source != "manual":
            profile.verdict = sanitize_for_json(_build_rule_based_verdict(question_comparisons))
            flag_modified(profile, "verdict")
    _embed_study_metrics_in_report(survey)
    flag_modified(survey, "test_suite_report")


@router.post("/attach-and-compare/{survey_id}")
async def attach_and_compare(
    survey_id: str, payload: ValidationPayload, db: Session = Depends(get_db)
):
    """
    Attach synthetic / real responses to a survey and run the comparison in one call.
    """
    survey = db.query(Survey).filter(Survey.id == survey_id).first()
    if not survey:
        raise HTTPException(status_code=404, detail="Survey not found")

    try:
        survey.synthetic_responses = payload.synthetic_responses
        survey.real_responses = payload.real_responses
        db.add(survey)
        db.commit()
        db.refresh(survey)

        return _run_comparison(survey, payload.synthetic_responses, payload.real_responses, db)
    except Exception as e:
        db.rollback()
        raise HTTPException(status_code=500, detail=str(e))


@router.post("/compare/{survey_id}")
async def compare_data(survey_id: str, db: Session = Depends(get_db)):
    """Compare using whatever data is already stored on the survey."""
    survey = db.query(Survey).filter(Survey.id == survey_id).first()
    if not survey:
        raise HTTPException(status_code=404, detail="Survey not found")

    try:
        return _run_comparison(
            survey, survey.synthetic_responses or [], survey.real_responses or [], db
        )
    except Exception as e:
        db.rollback()
        raise HTTPException(status_code=500, detail=str(e))


@router.get("/results/{survey_id}")
async def get_results(survey_id: str, db: Session = Depends(get_db)):
    survey = db.query(Survey).filter(Survey.id == survey_id).first()
    if not survey or not survey.test_suite_report:
        raise HTTPException(status_code=404, detail="Results not found")
    
    result = {
        "survey_id": survey_id,
        "accuracy": survey.accuracy_score,
        "results": survey.test_suite_report,
        "overall_accuracy": survey.accuracy_score,
        "tests": survey.test_suite_report.get("tests", []),
        "test_summary": survey.test_suite_report.get("test_summary", {}),
        "recommendations": survey.test_suite_report.get("recommendations", []),
    }
    
    # Add question comparisons if available
    if isinstance(survey.test_suite_report, dict):
        qc = survey.test_suite_report.get("question_comparisons")
        if qc:
            logger.info(f"Retrieved {len(qc)} question comparisons from test_suite_report for survey {survey_id}")
            result["question_comparisons"] = qc
        else:
            logger.warning(f"No question_comparisons found in test_suite_report for survey {survey_id}. Keys: {list(survey.test_suite_report.keys())}")
            result["question_comparisons"] = []
    else:
        logger.warning(f"test_suite_report is not a dict (type: {type(survey.test_suite_report)}) for survey {survey_id}")
        result["question_comparisons"] = []
    
    # Add survey metadata
    sm = None
    if isinstance(survey.test_suite_report, dict):
        sm = survey.test_suite_report.get("study_metrics")
    result["study_metrics"] = sanitize_for_json(
        sm
        or {
            "actions_data_points": survey.actions_data_points,
            "neuroscience_data_points": survey.neuroscience_data_points,
            "contextual_layer_data_points": survey.contextual_layer_data_points,
            "avg_similarity": survey.avg_similarity if survey.avg_similarity is not None else survey.accuracy_score,
            "directional_alignment": survey.directional_alignment,
            "avg_prediction_accuracy": getattr(survey, "avg_prediction_accuracy", None),
            "avg_relationship_strength": getattr(survey, "avg_relationship_strength", None),
            "checks_passed": survey.checks_passed,
        }
    )
